package dreamschool.csourse.chapter08;

public class MaxMin {
	private int num;
	private int max;
	private int min;
	
	public MaxMin(int num, int max, int min) {
		this.num = num;
		this.max = max;
		this.min = min;
	}
	public int getNum() {
		return num;
	}
	public int getMax() {
		return max;
	}
	public int getMin() {
		return min;
	}
	
	

}
